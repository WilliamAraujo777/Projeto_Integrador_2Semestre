    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // Verifica se está inserindo ou alterando
        if (alterarProduto == null) {
            // Resgata os dados inseridos pelo usuário
            String nomeProd = txtNomeProd.getText();
            double precProd = Double.parseDouble(txtPrecProd.getText());
            int qtdProd = Integer.parseInt(txtQtdProd.getText());
            String catProd = cbCatProd.getSelectedItem().toString();
            int idCat = 0;

            // Retornando o ID da categoria
            if (catProd.equalsIgnoreCase("camisa")) {
                idCat = 1;
            } else if (catProd.equalsIgnoreCase("calça")) {
                idCat = 2;
            } else if (catProd.equalsIgnoreCase("tênis")) {
                idCat = 3;
            } else if (catProd.equalsIgnoreCase("bermuda")) {
                idCat = 4;
            } else if (catProd.equalsIgnoreCase("meia")) {
                idCat = 5;
            } else if (catProd.equalsIgnoreCase("bola")) {
                idCat = 6;
            }

            //Passar os dados para o objeto
            Produto novoProduto = new Produto(nomeProd, precProd, qtdProd, idCat);

            // Passando o objeto para o banco
            boolean retorno = ProdutoDAO.salvarProduto(novoProduto);

            if (retorno) {
                JOptionPane.showMessageDialog(rootPane, "Sucesso!");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Falha!");
            }
        } else {
            // Modo de alteração
            String nomeProd = txtNomeProd.getText();
            double precProd = Double.parseDouble(txtPrecProd.getText());
            int qtdProd = Integer.parseInt(txtQtdProd.getText());
            String catProd = cbCatProd.getSelectedItem().toString();
            int idCat = 0;

            // Retornando o ID da categoria
            if (catProd.equalsIgnoreCase("camisa")) {
                idCat = 1;
            } else if (catProd.equalsIgnoreCase("calça")) {
                idCat = 2;
            } else if (catProd.equalsIgnoreCase("tênis")) {
                idCat = 3;
            } else if (catProd.equalsIgnoreCase("bermuda")) {
                idCat = 4;
            } else if (catProd.equalsIgnoreCase("meia")) {
                idCat = 5;
            } else if (catProd.equalsIgnoreCase("bola")) {
                idCat = 6;
            }

            alterarProduto.setNomeProduto(nomeProd);
            alterarProduto.setPrecoProduto(precProd);
            alterarProduto.setQtdProduto(qtdProd);
            alterarProduto.setIdCategoria(idCat);

            // Passar o objeto para a DAO
            boolean retorno = ProdutoDAO.alterar(alterarProduto);

            if (retorno) {
                JOptionPane.showMessageDialog(rootPane, "Sucesso!");
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Falha!");
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

